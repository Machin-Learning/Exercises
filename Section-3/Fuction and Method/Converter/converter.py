def minitsToHours(minits):
    return print(minits/60)

def secToHours(seconds):
    return print(seconds/3600)

def Hours(minits,seconds):
    return print(minits/60 + seconds/3600)

def degTofar(cel):
    return print((cel*(9/5))+32)

def feetToinch(feet):
    return print(feet*12)


